const langs = ['en','mk','fb'];
const langLabels = {en:'EN', mk:'МК', fb:'FB'};
let langIdx = 0;
try {
  const saved = localStorage.getItem('nmLang');
  if (saved && langs.includes(saved)) langIdx = langs.indexOf(saved);
} catch(e) {}

function applyLang(l) {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const k = el.getAttribute('data-i18n');
    if (i18n[l][k] !== undefined) el.innerHTML = i18n[l][k];
  });
  document.getElementById('lang-btn').textContent = langLabels[l];
  darkBtn.textContent = isDark ? i18n[l]['nav-light'] : i18n[l]['nav-dark'];
  updateBadge();
}

document.getElementById('lang-btn').addEventListener('click', () => {
  langIdx = (langIdx + 1) % langs.length;
  try { localStorage.setItem('nmLang', langs[langIdx]); } catch(e) {}
  applyLang(langs[langIdx]);
});

applyLang(langs[langIdx]);